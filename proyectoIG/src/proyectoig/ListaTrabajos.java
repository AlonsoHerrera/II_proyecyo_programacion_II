    private void cargar() {
        log.cargarTrabajos();
        modelo = new DefaultListModel();
        listClientes.setModel(modelo);
        trabajoPI[] temporal = log.getTrabajos();
        for (int i = 0; i < temporal.length; i++) {
            modelo.addElement(temporal[i]);
        }
    }


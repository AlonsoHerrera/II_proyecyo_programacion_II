    private void btnReparadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReparadoActionPerformed
        int seleccion = listClientes.getSelectedIndex();
        log.cargarTrabajos();
        trabajoPI[] temp = log.getTrabajos();
        trabajoPI tra = new trabajoPI();
        tra.setCedula2(temp[seleccion].getCedula2());
        tra.setColor(temp[seleccion].getColor());
        tra.setEstado(temp[seleccion].isEstado());
        tra.setMarca(temp[seleccion].getMarca());
        tra.setModelo(temp[seleccion].getModelo());
        tra.setProblema(temp[seleccion].getProblema());
        tra.setReparado("Reparado");
        log.eliminarTrabajos(tra, temp[seleccion].getCedula2());
        log.agregarTrabajos(tra);

    }//GEN-LAST:event_btnReparadoActionPerformed


    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
 log.cargarTrabajos();
        trabajoPI tra = new trabajoPI();
        int e = listClientes.getSelectedIndex();
        trabajoPI[] temp = log.getTrabajos();
        for (int i = 0; i < temp.length; i++) {
            if (temp[i].getCedula2() == temp[e].getCedula2()) {
                tra.setCedula2(temp[e].getCedula2());
                tra.setColor(temp[e].getColor());
                tra.setEstado(temp[e].isEstado());
                tra.setMarca(temp[e].getMarca());
                tra.setModelo(temp[e].getModelo());
                tra.setProblema(temp[e].getProblema());
                tra.setReparado(temp[e].getReparado());
                int precio = Integer.parseInt(JOptionPane.showInputDialog("digite el costo de reparacion"));
            total += precio;
            totalField.setText(Integer.toString(total));
            }

            log.eliminarTrabajos(tra, temp[e].getCedula2());
        }
    }//GEN-LAST:event_btnRetirarActionPerformed


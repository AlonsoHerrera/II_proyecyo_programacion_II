    private void btnBuscarNCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarNCActionPerformed
        trabajoPI[] temp = log.getTrabajos();
        modelo = new DefaultListModel();
        listClientes.setModel(modelo);
        for (int i = 0; i < temp.length; i++) {
            if (Integer.parseInt(txtBuscarNC.getText()) == temp[i].getCedula2()) {
                modelo.addElement(temp[i]);

            }
        }
    }//GEN-LAST:event_btnBuscarNCActionPerformed

